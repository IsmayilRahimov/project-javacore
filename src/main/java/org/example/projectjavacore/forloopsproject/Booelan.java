package org.example.projectjavacore.forloopsproject;

public class Booelan {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean c = a && b;
        boolean d = a || b;
        boolean e = !a;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(a ^ b);
    }
}
