package org.example.projectjavacore.variables;

public class ControlStatements {
    public static void main(String[] args) {
        int bal = 85;
        if (bal >= 90) {
            System.out.println("Ela");
        } else if (bal >= 80) {
            System.out.println("Yaxsi");
        } else {
            System.out.println("Oxu-Calis");
        }

        System.out.println();

        for (int i = 0; i <= 5; i++) {
            System.out.println(i + "Edediniz");
        }
    }
}
